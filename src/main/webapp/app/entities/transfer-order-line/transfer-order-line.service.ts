import axios from 'axios';

import buildPaginationQueryOpts from '@/shared/sort/sorts';

import { ITransferOrderLine } from '@/shared/model/transfer-order-line.model';

const baseApiUrl = 'api/transfer-order-lines';

export default class TransferOrderLineService {
  public find(id: number): Promise<ITransferOrderLine> {
    return new Promise<ITransferOrderLine>((resolve, reject) => {
      axios
        .get(`${baseApiUrl}/${id}`)
        .then(res => {
          resolve(res.data);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  public retrieve(paginationQuery?: any): Promise<any> {
    return new Promise<any>((resolve, reject) => {
      axios
        .get(baseApiUrl + `?${buildPaginationQueryOpts(paginationQuery)}`)
        .then(res => {
          resolve(res);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  public delete(id: number): Promise<any> {
    return new Promise<any>((resolve, reject) => {
      axios
        .delete(`${baseApiUrl}/${id}`)
        .then(res => {
          resolve(res);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  public create(entity: ITransferOrderLine): Promise<ITransferOrderLine> {
    return new Promise<ITransferOrderLine>((resolve, reject) => {
      axios
        .post(`${baseApiUrl}`, entity)
        .then(res => {
          resolve(res.data);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  public update(entity: ITransferOrderLine): Promise<ITransferOrderLine> {
    return new Promise<ITransferOrderLine>((resolve, reject) => {
      axios
        .put(`${baseApiUrl}/${entity.id}`, entity)
        .then(res => {
          resolve(res.data);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  public partialUpdate(entity: ITransferOrderLine): Promise<ITransferOrderLine> {
    return new Promise<ITransferOrderLine>((resolve, reject) => {
      axios
        .patch(`${baseApiUrl}/${entity.id}`, entity)
        .then(res => {
          resolve(res.data);
        })
        .catch(err => {
          reject(err);
        });
    });
  }
}
